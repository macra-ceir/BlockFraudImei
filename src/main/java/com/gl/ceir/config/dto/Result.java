package com.gl.ceir.config.dto;


public class Result {

    String message;

    public Result(String message) {
        this.message = message;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

    @Override
    public String toString() {
        return "Result{" + "message=" + message + '}';
    }

}
